# -*- coding: utf-8 -*-
"""
================================================================
برنامج تقدير الطالب
================================================================
يقوم البرنامج بطلب درجة الطالب بشكل متكرر ويحدد التقدير المناسب
(ضعيف، جيد، جيد جداً، ممتاز) حتى يكتب المستخدم "خروج"
"""

# عرض عنوان البرنامج
print("=" * 40)
print("     برنامج تقدير الطالب")
print("=" * 40)
print("النطاق المسموح: 0 - 100")
print('للخروج من البرنامج اكتب "خروج"')
print("-" * 40)

# حلقة لا نهائية لتكرار طلب الدرجة
while True:
    
    # طلب إدخال الدرجة من المستخدم
    user_input = input("\nأدخل درجة الطالب: ")
    
    # شرط الخروج من البرنامج
    if user_input == "خروج":
        print("\nشكراً لاستخدام البرنامج. مع السلامة!")
        break
    
    # =========================================================
    # التغلب على المشاكل (معالجة الأخطاء)
    # =========================================================
    
    # التحقق من أن المستخدم أدخل قيمة (ليست فارغة)
    if user_input == "":
        print("❌ خطأ: لم تقم بإدخال أي قيمة. الرجاء إدخال درجة أو كتابة خروج")
        continue  # العودة لطلب الإدخال مرة أخرى
    
    # محاولة تحويل المدخلات إلى رقم (معالجة الأخطاء)
    try:
        grade = float(user_input)  # تحويل النص إلى رقم عشري
    except ValueError:
        # هذه الرسالة تظهر إذا أدخل المستخدم حروفاً أو رموزاً غير رقمية
        print(f"❌ خطأ: '{user_input}' ليست رقماً صحيحاً. الرجاء إدخال رقم بين 0 و100")
        continue  # العودة لطلب الإدخال مرة أخرى
    
    # التحقق من أن الدرجة ضمن النطاق المسموح (0 إلى 100)
    if grade < 0 or grade > 100:
        print("❌ خطأ: الدرجة يجب أن تكون بين 0 و 100")
        continue  # العودة لطلب الإدخال مرة أخرى
    
    # =========================================================
    # تحديد التقدير بناءً على الدرجة
    # =========================================================
    
    # شرط التقدير ضعيف (أقل من 50)
    if grade < 50:
        estimation = "ضعيف"
    
    # شرط التقدير جيد (من 50 إلى أقل من 65)
    elif grade < 65:
        estimation = "جيد"
    
    # شرط التقدير جيد جداً (من 65 إلى أقل من 85)
    elif grade < 85:
        estimation = "جيد جداً"
    
    # شرط التقدير ممتاز (85 فما فوق)
    else:
        estimation = "ممتاز"
    
    # عرض النتيجة للمستخدم
    print(f"✅ الدرجة {grade} → التقدير: {estimation}")

# نهاية البرنامج
